function hashString(input: string): number {
  let hash = 2166136261;

  for (let i = 0; i < input.length; i += 1) {
    hash ^= input.charCodeAt(i);
    hash +=
      (hash << 1) +
      (hash << 4) +
      (hash << 7) +
      (hash << 8) +
      (hash << 24);
  }

  return hash >>> 0;
}

function seededRandom(seed: number): () => number {
  let value = seed;

  return () => {
    value = (value * 1664525 + 1013904223) >>> 0;
    return value / 4294967296;
  };
}

function seededShuffle<T>(items: T[], seedText: string): T[] {
  const shuffled = [...items];
  const random = seededRandom(hashString(seedText));

  for (let i = shuffled.length - 1; i > 0; i -= 1) {
    const j = Math.floor(random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }

  return shuffled;
}

export function selectCatalogTracks(params: {
  laneId: string;
  seed: string;
  count?: number;
}): CatalogSeedTrack[] {
  const { laneId, seed, count = 10 } = params;

  const laneTracks = getTracksByLane(laneId);

  if (laneTracks.length === 0) {
    return [];
  }

  return seededShuffle(laneTracks, `${laneId}:${seed}`).slice(0, count);
}

// youtubeVideoId가 non-empty string인 track만 "verified"로 본다 — 공백 문자열은 채워지지 않은 것으로 취급.
export function hasYoutubeVideoId(track: CatalogSeedTrack): boolean {
  return typeof track.youtubeVideoId === "string" && track.youtubeVideoId.trim().length > 0;
}

export function getVerifiedTracksByLane(laneId: string): CatalogSeedTrack[] {
  return getTracksByLane(laneId).filter(hasYoutubeVideoId);
}

// selectCatalogTracks와 동일한 seededShuffle을 사용하지만, youtubeVideoId가 채워진 track만 대상으로 한다.
// runtime이 이 결과만으로 충분하면 YouTube search API를 호출하지 않고 바로 DB에 저장할 수 있다.
export function selectVerifiedCatalogTracks(params: {
  laneId: string;
  seed: string;
  count?: number;
}): CatalogSeedTrack[] {
  const { laneId, seed, count = 10 } = params;

  const verifiedLaneTracks = getVerifiedTracksByLane(laneId);

  if (verifiedLaneTracks.length === 0) {
    return [];
  }

  return seededShuffle(verifiedLaneTracks, `${laneId}:${seed}:verified`).slice(0, count);
}

const VALID_ENERGIES: TrackEnergy[] = ["low", "medium", "high"];
const MIN_LANE_TRACK_COUNT = 5;

// fatal한 catalog 구조 문제(필수 필드 누락, 잘못된 energy 값) 외에는 throw하지 않는다.
// lane track count 부족이나 duplicate title+artist는 console.warn으로만 알린다.
// 같은 곡이 서로 다른 curation lane에 걸쳐 등장하는 것은 허용되므로, duplicate 검사는 lane 내부로 한정한다.
// youtubeVideoId는 현재 MVP에서 optional future field일 뿐이므로 이 검증에서는 다루지 않는다.
export function validateCatalog(): void {
  const laneCounts = new Map<string, number>();
  const seenTitleArtistByLane = new Map<string, Set<string>>();

  for (const track of ALL_SEED_TRACKS) {
    if (
      !track.laneId ||
      !track.title ||
      !track.artist ||
      !track.energy ||
      !track.moodTags ||
      !track.sceneTags
    ) {
      throw new Error(
        `Invalid catalog track: missing required field for "${track.title ?? "unknown"}" by "${track.artist ?? "unknown"}"`,
      );
    }

    if (!VALID_ENERGIES.includes(track.energy)) {
      throw new Error(
        `Invalid catalog track energy "${track.energy}" for "${track.title}" by "${track.artist}"`,
      );
    }

    // youtubeVideoId가 없는 것은 정상(점진적으로 채워지는 중)이므로 경고하지 않는다.
    // 단, 필드가 존재하는데 빈 문자열/공백뿐이면 잘못 입력된 것이므로 경고한다.
    if (track.youtubeVideoId !== undefined && track.youtubeVideoId.trim().length === 0) {
      console.warn(
        `Catalog track "${track.title}" by "${track.artist}" in lane "${track.laneId}" has a blank youtubeVideoId`,
      );
    }

    laneCounts.set(track.laneId, (laneCounts.get(track.laneId) ?? 0) + 1);

    let seenTitleArtist = seenTitleArtistByLane.get(track.laneId);
    if (!seenTitleArtist) {
      seenTitleArtist = new Set<string>();
      seenTitleArtistByLane.set(track.laneId, seenTitleArtist);
    }

    const titleArtistKey = `${track.title}::${track.artist}`;
    if (seenTitleArtist.has(titleArtistKey)) {
      console.warn(`Duplicate catalog track detected in lane "${track.laneId}": "${track.title}" by "${track.artist}"`);
    }
    seenTitleArtist.add(titleArtistKey);
  }

  for (const [laneId, count] of laneCounts) {
    if (count < MIN_LANE_TRACK_COUNT) {
      console.warn(`Catalog lane "${laneId}" has only ${count} track(s), below minimum ${MIN_LANE_TRACK_COUNT}`);
    }
  }
}
